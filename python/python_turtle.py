import turtle

t = turtle.Turtle()

t.up()
t.speed(5)
t.down()
t.shape('turtle')

# ---------------------------------------------------------------------------------------------------------------------------------------

for x in range(4):
    t.forward(50)
    t.right(90)
    # t.hideturtle()

# ---------------------------------------------------------------------------------------------------------------------------------------

t.fillcolor('#ff0')
t.begin_fill()
t.circle(100)
t.end_fill()
t.hideturtle()
# ---------------------------------------------------------------------------------------------------------------------------------------

t.right(75)
t.forward(100)

for x in range(5):
    t.right(144)
    t.forward(100)

turtle.done()