# asterisk operator

a, b, *c = 'Welcome'
print(a)
print(b)
print(c)

# ---------------------------------------------------------------------------------------------------------------------------------------

start, *middle, end = [1, 2, 3, 4, 5, 6, 7, 8, 9]
