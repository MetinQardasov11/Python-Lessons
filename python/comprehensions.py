
# Comprehensions: List, Set, Dictionary

# ! List Comprehensions
import random

data = [x for x in range(1, 11)]
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

random_nums = [random.randint(1, 100) for _ in range(10)]
print(random_nums)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = [x for x  in 'python']
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = [(i+1) * x for i, x in enumerate('python')]
data = [i * x for i, x in enumerate('python',1)]
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = [x for x in range(55) if x % 5 != 0]
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = ['ON' if x == 10 else x for x in range(55)]
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

from faker import Faker

fake = Faker('az_Az')
names = [(fake.name(), fake.email(), random.randint(20, 50)) for _ in range(10)]
print(names)

# ---------------------------------------------------------------------------------------------------------------------------------------

# ! Dictionary Comprehensions

data = {x:x+1 for x in range(10)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {x:'python' for x in range(10)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {'programming':'python' for x in range(10)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {x:x**2 for x in range(1, 11)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {i:x for i, x in enumerate('python')}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

fake = Faker('az_Az')
data = {fake.name():random.randint(20, 50) for _ in range(10)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {x:x**2 for x in range(1, 11) if x < 6}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {'yeddi' if x == 7 else x: 'super' if x == 7 else x**2 for x in range(1, 11)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

# ! Dictionary Comprehensions

data = {x for x in range(10)}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

data = {x for x in 'salam'}
print(data)

# ---------------------------------------------------------------------------------------------------------------------------------------

fake = Faker('az_Az')
data = {(fake.email(), fake.phone_number()) for x in range(10)}
print(data)

